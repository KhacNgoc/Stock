    public static void select(Connection conn, String u, String p){
        String selectQuery = "SELECT * FROM users WHEN username=? AND password=?;";
        if (conn != null){
            try{
                PreparedStatement prest = conn.prepareStatement(selectQuery);
                prest.setString(1, u);
                prest.setString(2, p);
                prest.executeUpdate();
                prest.close();
            }catch(SQLException e){
                e.printStackTrace();
            }
        }
    }
